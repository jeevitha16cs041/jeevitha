import java.util.*;
import java.lang.*;
import java.io.*;

	class Alphabet {
	
	    public static void main(String[] args) {
	
	        char c = 'd';
	
	        if( (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	        {
	            System.out.println(c + " Alphabet.");
	        }
	        else
	        {
	            System.out.println(c + "No");
	        }
	    }
	
	}
